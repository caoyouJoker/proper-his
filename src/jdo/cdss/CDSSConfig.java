	public List<String> getStrs(String s){
		
		String[] ss = s.split(";");
		
		List<String> l = new ArrayList<String>();
		
		for (String string : ss) {
			if(!l.contains(string)){
				l.add(string);
			}
		}
		
		return l;
	}
	
